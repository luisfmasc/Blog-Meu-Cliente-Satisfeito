					<h3> Desperdício</h3>
					<p>Não deixe de analisar todos os ângulos que a pesquisa de satisfação ter fornecer: analise os padrões, por exemplo, veja como as pessoas de certa faixa etária respondem determinada pergunta, se o pensamentos delas é parecido naquele quesito. Analise se uma determinada pergunta é respondida diferentemente por diferentes gêneros.
					<h3>Respondentes Prejudiciosos</h3>
					<p>Não se permita aceitar pesquisas feitas nas redes sociais imediatas a seus funcionários, as opiniões de familiares e parentes nunca serão totalmente sinceras ou precisas. Se não souber como atingir mais pessoas na sua busca, contrate uma agência de pesquisa de mercado, muitas possuem ferramentas de campanha de pesquisa cem porcento virtuais capazes de encaixar em qualquer tamanho de orçamento. </p>
					<h3>Perguntas Mal Estruturadas</h3>
					<p>Não se deixe produzir uma base de opiniões não confiável: garanta que todas as perguntas possuam respostas mutuamente excludentes. Garanta também que elas tratem de um único tema - ninguém gosta de dar uma resposta que implique numa outra idéia - perguntas com idéias implicitas farão as pessoas responderem de forma imprecisa.</p>
					<h3>Falta de Inovação</h3>
					<p>Não adianta preparar seu negócio para decolar com técnicas de NPS e Crescimento Previsível sem conhecer os desafios do seu mercado. Lembre-se de que você está no meio online, não trate sua pesquisa como se fosse feita de caneta e papel. Pense em todas as possibilidades ricas de interação que podem servir para aumentar a precisão da resposta: imagens, vídeos, animações. Pense em como o respondente pode dar informações muito mais interessantes se o método de resposta for interativo ao invés de uma simples caixa de texto. Pergunte sobre o tema quando estiver buscando uma agência de pesquisas, se eles não forem de inovar constantemente em seus questionários, escolha outra empresa.</p>
