						<p>Ainda não conhece a <b>metodologia NPS</b>? Então saiba o que as grandes empresas como Amazon, Starbucks, e Nike estão usando para melhorar seus resultados.</p>
						<p>NPS, ou <b>Net Promoter Score</b>, hoje é é usado por<b> 95% das 500 maiores empresas do mundo</b>, segundo a revista Fortune. <a href="http://meuclientesatisfeito.com/nps-o-que-e.php" target="_blank" rel="noopener">Neste outro artigo</a> já falamos mais sobre o que é e como funciona o NPS. </p>
						<p>Para melhorar ainda mais seu conhecimento sobre o assunto, listamos <b>3 fatos sobre NPS que você precisa saber:</b></p>
						<h2>Fato #1: como surgiu o NPS</h2>
						<p>A metodologia do Net Promoter Score (NPS) foi criada por <b>Fred Reichheld</b>, nos EUA, com intuito de medir o índice de lealdade dos clientes por meio de uma única pergunta “<b>A pergunta definitiva</b>” expressão que também é título de dois livros do autor que são a base da metodologia, lançados após a primeira apresentação sobre o tema em 2003.</p>
						<p> Você deve estar se perguntando: "Que pergunta milagrosa é esta?". A gente te responde:</p>
						<blockquote class="bgAzulEscuro">
							<p><b>“Qual a probabilidade de você recomendar [empresa X] a um amigo ou colega?”</b></p>
						</blockquote>
						<p>Esta pergunta é facilmente respondida uma <b>escala de 0 a 10,</b> onde é possível mensurar o grau de satisfação dos seus clientes independentemente do seu tipo de negócio, com base em três tipos de perfis:</p>
						<ul>
							<li>Notas <b class="Vermelho">de 0 a 06 – Clientes Detratores</b> - São aqueles clientes insatisfeitos, que não voltariam a fazer negócios com a sua empresa e que a criticam em público.</li>
							<li>Notas <b>de 7 e 8 – Clientes Neutros</b> - Aqueles que consomem o necessário e não são leais, nem promovem a marca.</li>
							<li>Notas <b class="verde">de 9 e 10 – Clientes Promotores </b> - São aqueles que tiveram uma experiência feliz com a empresa, são engajados e tendem, como o próprio nome diz, a promover a marca.</p></li>
						</ul>
						<h2>Fato #2: as vantagens de se usar o NPS</h2>
						<p>Aqui vai o top 3 de vantagens:</p>
						<ol>
							<li><b>Fácil entendimento pelos clientes</b>
							</li>
							<p> Este é um método que possui fácil assimilação por parte dos clientes, operadores e gestores. Como a pergunta principal é curta e objetiva, o cliente rapidamente entende, responde e sua empresa já começa a medir os resultados online. Caso queira fazer mais perguntas durante a pesquisa de satisfação, certifique-se de que todas estejam claras, objetivas e que tenham real necessidade para realizar melhorias na empresa.</p>
							<li><b>Agilidade na implementação de melhorias</b>
							</li>
							<p> O foco do NPS é melhorar o desempenho da empresa com base nos feedbacks recebidos, e, assim que tiver um volume de amostra significativo, já pode iniciar o planejamento de melhorias e dividir as tarefas para o time.<b> Tão importante quanto realizar a pesquisa de satisfação é atuar!</b> Não basta coletar dados e não fazer nada com eles.</p>
							<li><b>Benchmark</b>
							</li>
							<p> O NPS é um método muito utilizado por grandes empresas, inclusive pelas mesmas do seu segmento de mercado. Com conhecimento da concorrência e networking, é possível comparar o seu resultado com outras empresas e entender o quão bem (ou mal) você está. Mas fique atento: ninguém está ruim o suficiente que não tenha solução e nem bem o bastante que não possa melhorar!</p>
						</ol>
						<h2>Fato #3: com qual frequência o NPS deve ser aplicado</h2>
						<p> Isso deve ser definido de acordo com a estratégia de cada empresa, mas o ideal é que seja de 90 em 90 dias, para que não gere incômodo ao seu cliente e para que ele consiga perceber e avaliar as melhorias que foram aplicadas desde a última pesquisa.</p>
						<p>Você pode também dar feedback aos clientes, listando as melhorias que foram realizadas nos últimos meses. Além de gerar fidelização, faz com que seu time nunca se acomode e busque aperfeiçoamento contínuo!</p>
						<p>E aí, pensando na realidade da sua empresa hoje, <b>seus clientes são Promotores ou Detratores?</b>
