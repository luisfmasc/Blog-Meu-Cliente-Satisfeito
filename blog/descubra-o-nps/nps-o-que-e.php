				<p>NPS é a sigla para <b>Net Promoter Score</b>, uma metodologia para medir o <b>grau de satisfação dos clientes</b> com a empresa. Em tradução direta, significa "Nota Média de Indicação" e retrata em números o quão seu cliente está satisfeito com o atendimento, produto ou serviço prestado, além do grau de lealdade e recorrência.</p>
				<h2>Porque o NPS é importante para minha empresa?</h2>
				<p>Com certeza você já ouviu o bordão "O cliente sempre tem razão", certo? Bom, isto não é uma máxima, mas uma coisa é certa: com ou sem razão, o cliente pode levar sua empresa ao topo ou ao declínio. Como? É aí que entra o NPS!</p>
				<p>A satisfação do cliente está diretamente ligada ao <a href="https://surfedigital.io/blog/ltv-como-calcular/"> Lifetime Value</a> (ou LTV: valor do tempo de vida do cliente na sua empresa). A equação é simples:</p>
				<p><b>Cliente satisfeito = indicações + recompra + fidelização</b>.</p>
				<p><b>Cliente insatisfeito = detração + churn + perda de LTV</b>.</p>
				<p>Medir o NPS é importante para entender o quão saudável está a relação empresa x cliente e como isso se reflete nos resultados.</p>
				<p>Para te ajudar, reflita sobre as seguintes questões:</p>
				<ul>
					<li>Para você, a maioria dos seus clientes está satisfeito ou insatisfeito com sua empresa?</li>
					<li>Seus clientes recomendam a empresa?</li>
					<li>Qual a percentagem dos clientes que voltaram a comprar com sua empresa?</li>
					<li>Como você mede a eficácia entre o que a empresa promete como valores ou o que é prometido ao cliente e o que realmente está sendo entregue na ponta?</li>
				</ul>
				<p>Se você - CEO, diretor, gestor - não soube responder com segurança às perguntas acima, fique alerta pois pode haver um grave problema de integração do time e medição de resultados efetivos.</p>
				<p>Uma dica importante é: <b>não espere perder clientes para fazer uma pesquisa de satisfação</b>! A pesquisa pode ser feitas pelas seguintes razões:</p>
