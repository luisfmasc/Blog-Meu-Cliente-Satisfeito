<p></h1>
</br>
</br>
<p>NPS é a sigla para</span><b> Net Promoter Score,</b><span style="font-weight: 400;"> uma metodologia para medir o </span><b>grau de satisfação dos clientes</b> <span style="font-weight: 400;">com a empresa. Em tradução direta, significa "Nota Média de Indicação" e retrata em números o quão seu cliente está satisfeito com o atendimento, produto ou serviço prestado, além do grau de lealdade e recorrência.</p>

</br>

<h2><b>Porque o NPS é importante para minha empresa?</b></h2>
<span style="font-weight: 400;">Com certeza você já ouviu o bordão "O cliente sempre tem razão", certo? Bom, isto não é uma máxima, mas uma coisa é certa: com ou sem razão, o cliente pode levar sua empresa ao topo ou ao declínio. Como? É aí que entra o NPS!</span>

</br>

<span style="font-weight: 400;">A satisfação do cliente está diretamente ligada ao</span><a href="https://surfedigital.io/blog/ltv-como-calcular/"> <span style="font-weight: 400;">Lifetime Value</span></a><span style="font-weight: 400;"> (ou LTV: valor do tempo de vida do cliente na sua empresa). A equação é simples:</span>

</br>

<b>Cliente satisfeito = indicações + recompra + fidelização</b>

<b>Cliente insatisfeito = detração + churn + perda de LTV</b>

</br>

<span style="font-weight: 400;">Medir o NPS é importante para entender o quão saudável está a relação empresa x cliente e como isso se reflete nos resultados.</span>

</br>

<span style="font-weight: 400;">Para te ajudar, reflita sobre as seguintes questões:</span>
<ul>
 	<li><span style="font-weight: 400;">Para você, a maioria dos seus clientes está satisfeito ou insatisfeito com sua empresa?</span></li>
 	<li style="font-weight: 400;"><span style="font-weight: 400;">Seus clientes recomendam a empresa?</span></li>
 	<li style="font-weight: 400;"><span style="font-weight: 400;">Qual a percentagem dos clientes que voltaram a comprar com sua empresa?</span></li>
 	<li style="font-weight: 400;"><span style="font-weight: 400;">Como você mede a eficácia entre o que a empresa promete como valores ou o que é prometido ao cliente e o que realmente está sendo entregue na ponta?</span></li>
</ul>
<span style="font-weight: 400;">Se você -  CEO, diretor, gestor -  não soube responder com segurança às perguntas acima, fique alerta pois pode haver um grave problema de integração do time e medição de resultados efetivos. </span>

</br>

<span style="font-weight: 400;">Uma dica importante é: </span><b>não espere perder clientes para fazer uma pesquisa de satisfação!</b><span style="font-weight: 400;"> A pesquisa pode ser feitas pelas seguintes razões:</span>

</br>
