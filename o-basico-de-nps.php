						<p>Ainda não conhece a metodologia do NPS? Então saiba qual é o método utilizado por corporações que são referência no mundo inteiro.</p>
						<p>A metodologia do Net Promoter Scores (NPS) foi criada por Fred Reichheld, nos EUA, com intuito de medir o índice de lealdade dos clientes por meio de uma única pergunta: “Qual a probabilidade de você recomendar [ empresa X] a um amigo ou colega?”.</p>
						<p>Em uma escala de 0 a 10 é possível mensurar o grau de satisfação dos seus clientes, com base em três tipos de perfis:</p>
						<ul>
							<li>Notas <b>de 0 a 06 – Clientes Detratores</b> - São aqueles clientes insatisfeitos, que não voltariam a fazer negócios com a sua empresa e que a criticam em público.</li>
							<li>Notas <b>de 7 e 8 – Clientes Neutros</b> - Aqueles que consomem o necessário e não são leais, nem promovem a marca.</li>
							<li>Notas <b>de 9 e 10 – Clientes Promotores</b> - São aqueles que tiveram uma experiência feliz com a empresa, são engajados e dispõe, como o próprio nome diz, a promover a marca.</li>
						</ul>

						<p>Este é um método simples, flexível e confiável para medir a satisfação dos seus clientes por isso tão utilizado por organizações dos mais variados ramos para entender os pontos de melhoria nos processos e produtos das suas empresas.</p>
